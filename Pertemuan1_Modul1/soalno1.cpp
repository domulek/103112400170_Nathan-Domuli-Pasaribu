#include <iostream>
using namespace std;

int main (){
    int bil1, bil2;
    cout << "Masukkan Bilangan 1 : ";
    cin >> bil1;
    cout << "Masukkan Bilangan 2 : ";
    cin >> bil2;

    cout << "Penjumlahan : " << bil1 + bil2 << endl;
    cout << "Pengurangan : " << bil1 - bil2 << endl;
    cout << "Pembagian : " << bil1 / bil2 << endl;
    cout << "Perkalian : " << bil1 * bil2 << endl;

    return 0;
}